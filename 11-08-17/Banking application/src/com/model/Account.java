package com.model;

public class Account {
	// Variable for accountNo
	private int pAccountNo;
	// Variable for name
	private String pName;
	// Variable for balance
	private int pBalance;

	// Method to get balance
	public int getpBalance() {
		return pBalance;
	}

	// Method to set balance
	public void setpBalance(int pBalance) {
		this.pBalance = pBalance;
	}

	// Method to get accountNO
	public int getpAccountNo() {
		return pAccountNo;
	}

	// Method to set accountNO
	public void setpAccountNo(int pAccountNo) {
		this.pAccountNo = pAccountNo;
	}

	// Method to get name
	public String getpName() {
		return pName;
	}

	// Method to set name
	public void setpName(String pName) {
		this.pName = pName;
	}

}
